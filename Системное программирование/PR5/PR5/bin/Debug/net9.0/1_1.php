     <h1 itemprop="itemreviewed">Веб-сайты ASP.NET</h1><meta itemprop="rating" content="5.0"><span class="get_votes" itemprop="votes">211</span>
     <p class="maplink"><a href="../../base/level1/aspnet_info.php">ASP.NET</a> --- Веб-сайты ASP.NET</p>
             <p>Научившись создавать цельные веб-страницы, программист начинает задумываться об общей картине - т.е. о группировании большого числа вебстраниц в единый логически связанный веб-сайт. В разделе <a href="../../../ASP_NET/base/level1/base_aspnet_index.php">&laquo;Основы ASP.NET&raquo;</a> рассматривались некоторые основы этого процесса, такие как управление состоянием при переходе пользователя от одной страницы к другой и использование отдельных компонентов для выноса кода доступа к данных за пределы веб-страниц, чтобы они были доступны тогда, когда в них возникает потребность.</p>
             <p>Однако программист сталкивается также с такими задачами, как обеспечение логической связности всех страниц и упрощение навигации по вебсайту. В этой части мы рассмотрим вопросы, которые становятся актуальными, когда вы перестаете думать об отдельных страницах и переходите к планированию веб-приложения в целом.</p>
<p>Вначале мы рассмотрим пользовательские элементы управления, которые дают возможность применять блок интерфейса пользователя в нескольких страницах. Затем вы познакомитесь с еще двумя инструментами: темами, которые позволяют автоматически устанавливать свойства элемента управления, и мастер-страницами, которые позволяют многократно использовать один шаблон для стандартизации компоновки и содержимого во множестве страниц. Все вместе эти три инструмента обеспечивают, чтобы веб-приложение выглядело как логически связанное единое целое.</p>
<p>Также будет рассмотрена тема, связанная с предыдущими: применение карт сайтов и элементов управления навигацией для предоставления пользователям возможности перемещения по веб-сайту. И, наконец, вы научитесь переносить свои веб-приложения в производственную среду, перемещая их из компьютера разработки {или тестового сервера) на полноценный веб-сервер, на котором функционирует IIS.</p>
<p>Так же будет показано, как можно расширять веб-страницы с помощью дополнительных технологий: специальных элементов управления, GDI+ для создания графики вручную, JavaScript и Ajax и средства Web Parts, которое позволяет легко создавать веб-порталы.</p>
